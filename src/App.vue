<script setup>
import { reactive } from 'vue'

const serviceEmail = 'thanna96@gmail.com'

const form = reactive({
  name: '',
  phone: '',
  email: '',
  date: '',
  message: '',
})

const handleSubmit = () => {
  const subject = encodeURIComponent('A-Z Auto Service Request')
  const lines = [
    `Name: ${form.name}`,
    `Phone: ${form.phone || 'N/A'}`,
    `Email: ${form.email}`,
  ]

  if (form.date) {
    lines.push(`Preferred Date: ${form.date}`)
  }

  lines.push('Vehicle & Service Needs:', form.message)

  const body = encodeURIComponent(lines.join('\n'))
  window.open(`mailto:${serviceEmail}?subject=${subject}&body=${body}`);
  // window.location.href = `mailto:${serviceEmail}?subject=${subject}&body=${body}`

  Object.assign(form, {
    name: '',
    phone: '',
    email: '',
    date: '',
    message: '',
  })
}
</script>